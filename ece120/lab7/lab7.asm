; The program below searches through a table of values in memory and produces
; an 8x16  font based on 0s and 1s in the FONT_DATA. The character to print is
; given in memory location x5002, the character to use for 0s is in x5000, and
; the character to use for 1s is in x5001. The program first computes the
; location of the 16 rows of the font in memory by multiplying the character
; value in hex with 16 as every font is 16 rows long. The program then uses
; two loops to cycle between 8 columns and 16 rows and checks if the bit at
; the position is 1 or 0. This is done by checking if the MSB is 1 or 0. If the
; string is +ve, the MSB is 0 and if the string is -ve, the MSB is 1. The
; register is then left shifted 1 bit by adding to itself, ie. multiplying by 2.
; The sentinel is used to go to the next line.

; R0 = Character for printing
; R1 = Address of current line
; R2 = Content of current line
; R3 = Counter for finding starting address of character
; R4 = Counter for number of characters left in a row
; R5 = Counter for number of lines left
; R6 = Value of character for finding starting address of character

; Start

                .ORIG   x3000		; Start program at x3000

; Find starting address of character in FONT_DATA

                AND R1, R1, #0          ; Initialize R1 = 0
                LDI R6, CHAR            ; Load value of character to R6
                LD R3, LINES            ; Load number of lines to R3
MULTIPLY        ADD R1, R1, R6          ; Add R1 & R6
                ADD R3, R3, #-1         ; Decrement R3 by 1
                BRp MULTIPLY            ; If +ve, loop to MULTIPLY
                LD R3, FONT_ADDR	; Load R3 with FONT_ADDR		
                ADD R1, R1, R3		; Add R1 & R3

; Check 1 or 0 and print corresponding character 

                LD R5, LINES		; Load number of lines to R5
LOOP2_INIT      LD R4, CHARS		; Load number of characters to R4
                LDR R2, R1, #0          ; Load content in current line to R2
LOOP_INIT       ADD R2, R2, #0		; Set condition codes with respect to R2
                BRn NEG                 ; If -ve, move to NEG
                LDI R0, ZERO            ; Load R0 with Char for 0
                OUT			; Print R0
                BRnzp DECR              ; Always, move to DECR
NEG             LDI R0, ONE             ; Load R0 with Char for 1
                OUT			; Print R0
DECR            ADD R2, R2, R2		; Multiply R2 * 2
                ADD R4, R4, #-1         ; Decrement R4 by 1
                BRp LOOP_INIT           ; If +ve, move to LOOP_INIT
                LD R0, SENTINEL		; Load R0 with SENTINEL
                OUT			; Print R0
                ADD R1, R1, #1		; Increment R1 by 1
                ADD R5, R5, #-1		; Decrement R5 by 1
                BRp LOOP2_INIT		; If +ve, move to LOOP2_INIT

; Stop execution

                HALT			; Halt program

; Labels

FONT_ADDR       .FILL   FONT_DATA	; Address of FONT_DATA
CHAR            .FILL   x5002           ; Address of character to print
LINES           .FILL   #16             ; Number of lines
ZERO            .FILL   x5000		; Address of character for 0s
ONE             .FILL   x5001		; Address of character for 1s
CHARS           .FILL   #8		; Number of characters
SENTINEL        .FILL   x0A		; Value of sentinel

